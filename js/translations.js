// ─── Language engine ───────────────────────────────────────────────────────

function getLang() {
  return localStorage.getItem('fd_lang') || 'fr';
}

function setLang(lang) {
  localStorage.setItem('fd_lang', lang);
  applyLang(lang);
}

function applyLang(lang) {
  const t = TRANSLATIONS[lang];
  if (!t) return;

  // 1. HTML dir + lang attribute
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang === 'ar' ? 'ar' : 'fr');

  // 2. Translate text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // 3. Translate HTML nodes (contains tags like <br>, <strong>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // 4. Translate placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
  });

  // 5. Update lang toggle buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // 6. Page title
  const titleKey = 'meta.title.' + (document.body.dataset.page || 'index');
  if (t[titleKey]) document.title = t[titleKey];
}

// ─── Init on DOMContentLoaded ─────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  // Wire up toggle buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });

  // Apply saved language
  applyLang(getLang());
});
